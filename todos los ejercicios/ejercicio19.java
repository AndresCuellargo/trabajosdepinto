public class ejercicio19 {
    public static void main(String[] args) {
        int N = 2; // Primer número par

        System.out.println(N); // Mostrar el primer número

        for (int K = 1; K <= 4; K++) {
            N += 2; // Incrementar en 2
            System.out.println(N); // Mostrar el número
        }
    }
}
